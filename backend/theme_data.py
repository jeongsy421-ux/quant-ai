def get_ticker_themes():
    """종목별 속한 테마 목록 반환"""
    ticker_themes = {}
    for theme_name, theme_data in THEMES.items():
        for ticker in theme_data["stocks"]:
            if ticker not in ticker_themes:
                ticker_themes[ticker] = []
            ticker_themes[ticker].append(theme_name)
    return ticker_themes

def get_all_tickers():
    """테마에 포함된 전체 티커 목록"""
    tickers = set()
    for theme in THEMES.values():
        tickers.update(theme["stocks"].keys())
    return list(tickers)

def get_theme_list():
    """테마 목록 요약 반환"""
    result = []
    for name, data in THEMES.items():
        result.append({
            "name": name,
            "category": data["category"],
            "emoji": data["emoji"],
            "description": data["description"],
            "leader": data["leader"],
            "stock_count": len(data["stocks"]),
            "keywords": data["keywords"],
        })
    return result
